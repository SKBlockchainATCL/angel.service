package angel.service.entrypostlike;

public class EntryPostLike {
	public String likeAt;
	public String isCanceled;
	public String serviceProgramPostId;
	public String UserId;
	
	public String getLikeAt() {
		return likeAt;
	}
	public void setLikeAt(String likeAt) {
		this.likeAt = likeAt;
	}
	public String getIsCanceled() {
		return isCanceled;
	}
	public void setIsCanceled(String isCanceled) {
		this.isCanceled = isCanceled;
	}
	public String getServiceProgramPostId() {
		return serviceProgramPostId;
	}
	public void setServiceProgramPostId(String serviceProgramPostId) {
		this.serviceProgramPostId = serviceProgramPostId;
	}
	public String getUserId() {
		return UserId;
	}
	public void setUserId(String userId) {
		UserId = userId;
	}

	
}
